package model;

public class Copia {
	private int id;
	private String dt;
	private int perdidas;
	private int prof;
	private int alu;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getDt() {
		return dt;
	}
	public void setDt(String dt) {
		this.dt = dt;
	}
	public int getPerdidas() {
		return perdidas;
	}
	public void setPerdidas(int perdidas) {
		this.perdidas = perdidas;
	}
	public int getProf() {
		return prof;
	}
	public void setProf(int prof) {
		this.prof = prof;
	}
	public int getAlu() {
		return alu;
	}
	public void setAlu(int alu) {
		this.alu = alu;
	}

}
